    private void salvarCd() {
        try {
            String titulo, data;
            int tempoRepro;
            titulo = txtTitulo.getText();
            data = txtData.getText();
            tempoRepro = Integer.parseInt(txtTempoRepro.getText());

            Cd cd = new Cd(titulo, data, tempoRepro);

            CdDao cdDao = new CdDao();
            cdDao.inserirCd(cd);
            //Mensagem
            JOptionPane.showMessageDialog(null, "Cd inserido com sucesso");

        } catch (Exception erro) {
            JOptionPane.showMessageDialog(null, "Erro no frmAddCd: " + erro);
        }
    }

    private void limpaCampos() {
        txtTitulo.setText("");
        txtData.setText("");
        txtTempoRepro.setText("");
    }
